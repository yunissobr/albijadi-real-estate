function translateAll(htmlId, ArabicTranslate) {
  var ele = document.querySelectorAll(htmlId);
  for (i = 0; i < ele.length; i++) {
    ele[i].textContent = ArabicTranslate;
  }
}

lng.onclick = function () {
  console.log("changed to arabic");
  if (getLanguage() == "en") {
    setLanguage("ar");
  } else {
    setLanguage("en");
  }
};

function getLanguage() {
  return localStorage.getItem("language") == null
    ? "ar"
    : localStorage.getItem("language");
}

function setLanguage(langauge) {
  console.log("language setted");
  localStorage.setItem("language", langauge);
  document.cookie = "ln=" + langauge;
  location.reload(true);
}

